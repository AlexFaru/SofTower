    private void DescuentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DescuentoActionPerformed
        String Id_Cliente;
        double Importe;
        double number;
        
        if(jTable_VentasTempo.getSelectedRows().length == 0){
            jLabel_Panel2.setVisible(true);
            jLabel_Panel2.setText("Seleciona un producto");
        }else{
            tablaModeloCLT = ventas.getModelo();
            int filas = jTable_VentasTempo.getSelectedRow();
            Id_Cliente = (String)tablaModeloCLT.getValueAt(filas,1);
            Importe = Double.parseDouble((String)tablaModeloCLT.getValueAt(filas,6));
            boolean canContinue;
            do {
            String userResponse = JOptionPane.showInputDialog(this, "Ingrese el descuento a realizar: ", 0);
            try {
                number = Double.parseDouble(userResponse);
                if(number < Importe){
                    canContinue = true;
                    ventas.setDescuento(Id_Cliente, number);
                    ventas.searchVentatemp(jTable_VentasTempo, num_registro, pageSize);
                    ventas.importes(jLabel_Total, filas);
                }else{
                    JOptionPane.showMessageDialog(null, "El valor del descuento\n"+"Es mayor que la compra");
                    canContinue=false;
                }
            }
            catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Agrega una cantidad valida", "Error", JOptionPane.ERROR_MESSAGE);
                canContinue = false;
            }
        } while (!canContinue);
            
        }
        
    }//GEN-LAST:event_DescuentoActionPerformed

